/**
 * SkillManifest — declarative on-disk contract for a skill.
 *
 * The existing `ReaperSkill` (src/adaptive/types.ts) is the *runtime*
 * shape produced by parsing a `SKILL.md`. The new `SkillManifest` is
 * the *folder-level* shape produced by parsing `skill.json` next to
 * the SKILL.md. The two complement each other; the new SkillRegistry
 * merges them.
 *
 * Trust values are an extension of the existing `SkillScope`:
 *   - "builtin"              shipped under src/skills/built-in
 *   - "user-trusted"         installed under ~/.reaper/skills
 *   - "project-untrusted"    installed under <workspace>/.reaper/skills
 *                            (default; promoted via `skill trust`)
 *   - "extension-inherited"  lives inside an extension's skills/ folder;
 *                            inherits the parent extension's trust
 *   - "draft"                generated by `skill create` or
 *                            `SkillAuthor.createSkillFromRunTrace`;
 *                            cannot be activated until promoted
 *
 * Categories are exactly the 17 built-in skill names defined in the
 * Skills/Extensions plan. The validator rejects unknown categories.
 */

export type SkillTrust =
  | "builtin"
  | "user-trusted"
  | "project-untrusted"
  | "extension-inherited"
  | "draft";

export type SkillCategory =
  | "repo-understanding"
  | "bug-fixing"
  | "test-failure-debugging"
  | "typescript-refactor"
  | "python-debugging"
  | "frontend-react-debugging"
  | "api-backend-debugging"
  | "security-review"
  | "performance-review"
  | "documentation-writing"
  | "terminal-bench-solving"
  | "swe-bench-solving"
  | "agent-runtime-debugging"
  | "session-persistence"
  | "prompt-enhancement";

