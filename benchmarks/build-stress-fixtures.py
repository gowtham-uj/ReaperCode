#!/usr/bin/env python3
"""Deterministically build the 5 stress fixture workspaces.

Sizes are calibrated against Reaper's shake/spillover constants:
- SHAKE_TRIGGER_PCT=50, softCap=270000 (default) → shake triggers at 135K tokens
- SPILLOVER_THRESHOLD_BYTES=8192
- PERSIST_THRESHOLD_CHARS=30000
"""
import json, os, random, sys
from pathlib import Path

ROOT = Path("/workspace/reapercode-main/benchmarks")
random.seed(20260704)

def write(path, content):
    p = ROOT / path
    p.parent.mkdir(parents=True, exist_ok=True)
    p.write_text(content)
    sz = len(content)
    print(f"  wrote {path} ({sz:,} bytes)")
    return sz

# ── Fixture 1: read-then-act-mid-compact ─────────────────────────────
# 600-line ledger with 11 NEEDLE markers every 50 lines.
print("Fixture 1: read-then-act-mid-compact")
lines = []
for i in range(1, 601):
    base = {"id": f"L-{i:04d}", "date": f"2026-{1+(i%12):02d}-{(i%28)+1:02d}",
            "account": f"4110-CUST-{chr(65+(i%26))}{i:04d}",
            "debit": round((i*7.13) % 9999.99 + 1, 2), "credit": 0.0,
            "memo": f"Invoice {1000+i} settled"}
    if i % 50 == 0:
        marker_n = i // 50
        base["marker"] = f"NEEDLE_{marker_n*5}K_TOKEN_OFFSET_{(i*7) % 100:02d}"
    lines.append(json.dumps(base, separators=(",", ":")))
write("read-then-act-mid-compact/payload/ledger.jsonl", "\n".join(lines) + "\n")

# ── Fixture 2: reread-huge-file ───────────────────────────────────────
# 50K-token log with deterministic line format; needle at line 12345.
print("Fixture 2: reread-huge-file")
log_lines = [
    "=== BIG LOG v1 ===",
    "bootseq beacon-7f3c seq=0 ts=1717600000",
]
for i in range(1, 50001):
    seq = i
    event = ["boot","auth.user","api.call","db.write","auth.user","file.read","api.call","db.write","db.read","shutdown"][i % 10]
    uid = f"user-{(i*31) % 1000:04d}"
    role = ["admin","user","svc","guest","admin"][i % 5]
    ts = 1717600000 + i
    if i == 12345:
        log_lines.append(f"line {i:05d} :: seq={seq} event={event} id={uid} ts={ts} role={role} MAGIC_TOKEN_REREAD_12345")
    else:
        log_lines.append(f"line {i:05d} :: seq={seq} event={event} id={uid} ts={ts} role={role}")
log_lines.append("=== END LOG ===")
write("reread-huge-file/payload/biglog.txt", "\n".join(log_lines) + "\n")

# ── Fixture 3: bash-giant-log-spillover ───────────────────────────────
# No payload file - bash generates it. Add a tiny seed script for reproducibility.
print("Fixture 3: bash-giant-log-spillover (no payload - generated by bash)")

# ── Fixture 4: many-write-acks ────────────────────────────────────────
# 30K-token README with needles.
print("Fixture 4: many-write-acks")
sections = []
sections.append("# ACME CORP OPERATIONS README\n")
sections.append("ACCOUNT_ID=ACME-998877\n")
sections.append("CONTACT=ops@acme.example\n")
sections.append("\n## Section 1: Operations Overview\n")
sections.append("This README documents the canonical operations procedures for ACME Corp.\n" * 50)
# Drop the cache-prefix canary at line ~200
sections.append("\n## Section 2: Authentication\n")
sections.append("CACHE_PREFIX_CANARY=CACHE-OK-XYZZY\n")
sections.append("All authentication tokens are rotated weekly via the ops service.\n" * 30)
for sec_i in range(3, 41):
    sections.append(f"\n## Section {sec_i}: Workflow {sec_i}\n")
    body = f"Workflow {sec_i} covers procedure {sec_i} for the {sec_i}-th operational domain.\n"
    sections.append(body * 60)
write("many-write-acks/payload/big-readme.txt", "".join(sections))

# ── Fixture 5: plan-then-many-edits ───────────────────────────────────
# 80-line C file with 100 markers; markers live in comments.
print("Fixture 5: plan-then-many-edits")
c_lines = []
c_lines.append("// target.c — synthetic source for shake stress test")
c_lines.append("#include <stdio.h>")
c_lines.append("#include <stdlib.h>")
c_lines.append("")
for i in range(1, 101):
    c_lines.append(f"int op_{i}(int x) {{")
    c_lines.append(f"  /* EDIT_POINT_{i:03d} */")
    c_lines.append(f"  return x + {i};")
    c_lines.append("}")
    c_lines.append("")
c_lines.append("int main(void) {")
c_lines.append("  int total = 0;")
for i in range(1, 21):
    c_lines.append(f"  total += op_{i}({i});")
c_lines.append(f"  printf(\"%d\\n\", total);")
c_lines.append("  return 0;")
c_lines.append("}")
write("plan-then-many-edits/payload/target.c", "\n".join(c_lines) + "\n")

print("Done.")